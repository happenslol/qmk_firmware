static void render_primary(void) {
    switch (get_highest_layer(layer_state)) {
        case _LOWER:
            oled_write_raw_P(oled_layer_lower, sizeof(oled_layer_lower));
            break;
        case _RAISE:
            oled_write_raw_P(oled_layer_raise, sizeof(oled_layer_raise));
            break;
        case _MENU:
            oled_write_raw_P(oled_layer_menu, sizeof(oled_layer_menu));
            break;
        case _NAV:
            oled_write_raw_P(oled_layer_nav, sizeof(oled_layer_nav));
            break;
        case _FN:
            oled_write_raw_P(oled_layer_func, sizeof(oled_layer_func));
            break;
        default:
            oled_write_raw_P(oled_layer_base, sizeof(oled_layer_base));
    }
}

bool oled_task_user(void) {
    if (is_keyboard_master()) {
        render_primary();
    } else {
        oled_write_raw_P(oled_default, sizeof(oled_default));
    }
    return false;
}

